"""RULER benchmark loader + official scorer.

Each task's ``validation.jsonl`` (generated by NVIDIA's RULER prepare.py with
our Llama tokenizer at 8k context) yields ``{index, input, outputs, length}``.
The raw ``input`` is split into (context, question, answer_prefix) and wrapped in
the model's chat template exactly like kvpress ``pipeline.preprocess``: the
[context + question] go inside the ``user`` turn and the answer prefix is placed
AFTER the ``assistant`` header (``build_ruler_prompt``). Feeding the string raw
(no chat template) is wrong for an instruct model and diverges from kvpress — the
scripts build the templated prompt and encode it with ``add_special_tokens=False``.

Scoring (verbatim semantics from NVIDIA RULER / kvpress
``evaluation/benchmarks/ruler/calculate_metrics.py``)
------------------------------------------------------
* control characters ``[\\x00-\\x1f]`` are stripped from the prediction first;
* ``qa_*`` tasks       -> ``string_match_part``: 1 if ANY reference substring is
                          present (aliases of one gold answer), max over refs;
* every other task     -> ``string_match_all``: FRACTION of reference substrings
                          present (multi-needle tasks expect every needle);
* the score is a percentage in ``[0, 100]``, rounded to 2 dp, averaged over the
  task's samples.

``string_match_all`` here is identical to KVCache-Factory ``metrics.py`` and to
kvpress; ``string_match_part`` matches kvpress's ``qa`` branch. Reporting these
makes our RULER numbers directly comparable to both libraries.
"""

from __future__ import annotations

import json
import os
import re
from typing import List

# All RULER tasks we have data for at 8k. qa_* use part-match; the rest all-match.
RULER_TASKS = [
    "niah_multikey_1",
    "niah_multikey_2",
    "niah_multikey_3",
    "niah_multivalue",
    "niah_multiquery",
    "vt",
    "qa_2",
]

# Per-task generation budget (NVIDIA RULER constants.py / kvpress MAX_NEW_TOKENS,
# keyed by task category = name.split('_')[0]).
_MAX_NEW_BY_CATEGORY = {
    "niah": 128,
    "vt": 30,
    "cwe": 120,
    "fwe": 50,
    "qa": 32,
}

_CONTROL_CHARS = re.compile(r"[\x00-\x1f]")


def task_category(task: str) -> str:
    return task.split("_")[0]


def max_new_tokens(task: str) -> int:
    return _MAX_NEW_BY_CATEGORY[task_category(task)]


def load_ruler(task: str, data_root: str) -> List[dict]:
    """Return a list of {index, input, outputs, length} samples for one task."""
    path = os.path.join(data_root, task, "validation.jsonl")
    samples = []
    with open(path) as f:
        for line in f:
            samples.append(json.loads(line))
    return samples


# Per-category split of the raw RULER ``input`` into context / question /
# answer_prefix, verbatim from kvpress
# ``evaluation/benchmarks/ruler/create_huggingface_dataset.py`` (QUESTION_PATTERNS
# / ANSWER_PATTERNS). Used so the chat template can wrap [context+question] and
# place the answer prefix AFTER the assistant header, exactly like kvpress.
QUESTION_PATTERNS = {
    "niah": re.compile(r"What (?:is|are all) the special magic"),
    "vt": re.compile(r"Question: Find all variables that are assigned the value"),
    "cwe": re.compile(r"Question: What are the 10 most common words in the above list\?"),
    "fwe": re.compile(r"Question: Do not provide any explanation\."),
    "qa": re.compile(r"Answer the question based on the given documents\."),
}
ANSWER_PATTERNS = {
    "niah": re.compile(r"The special magic"),
    "vt": re.compile(r"Answer:"),
    "cwe": re.compile(r"Answer:"),
    "fwe": re.compile(r"Answer:"),
    "qa": re.compile(r"Answer:"),
}


def split_context_question(task: str, text: str) -> tuple[str, str, str]:
    """Split a raw RULER ``input`` into (context, question, answer_prefix)."""
    cat = task_category(task)
    qp, ap = QUESTION_PATTERNS[cat], ANSWER_PATTERNS[cat]
    idx = list(qp.finditer(text))[-1].start()
    context, qa = text[:idx], text[idx:]
    a = ap.search(qa).start()
    return context, qa[:a], qa[a:]


def build_ruler_prompt(task: str, input_str: str, tokenizer) -> str:
    """Chat-templated RULER prompt string, matching kvpress ``pipeline.preprocess``.

    Wraps [context + question] in the model chat template with
    ``add_generation_prompt=True`` and appends the answer prefix after the
    assistant header. Falls back to ``bos + input`` for template-less tokenizers.
    """
    context, question, answer_prefix = split_context_question(task, input_str)
    if getattr(tokenizer, "chat_template", None) is None:
        bos = getattr(tokenizer, "bos_token", "") or ""
        return bos + context + question + "\n" + answer_prefix
    sep = "#" * (len(context) + 10)
    templated = tokenizer.apply_chat_template(
        [{"role": "user", "content": context + sep}],
        add_generation_prompt=True,
        tokenize=False,
    )
    ctx_str, q_suffix = templated.split(sep)
    return ctx_str + question + q_suffix + answer_prefix


def _clean(pred: str) -> str:
    """Strip control chars and surrounding whitespace (kvpress preprocessing)."""
    return _CONTROL_CHARS.sub("", pred.strip()).strip()


def string_match_part(pred: str, refs: List[str]) -> float:
    """1.0 if ANY reference substring is in the prediction (case-insensitive).

    Used for qa_* tasks whose ``refs`` are aliases of a single gold answer.
    Matches kvpress ``string_match_part`` (per-sample, before x100).
    """
    p = _clean(pred).lower()
    return max((1.0 if r.lower() in p else 0.0) for r in refs) if refs else 0.0


def string_match_all(pred: str, refs: List[str]) -> float:
    """Fraction of reference substrings present in the prediction.

    Used for multi-needle tasks (niah_multi*, vt) where every ``ref`` must
    appear. Matches KVCache-Factory / kvpress ``string_match_all`` (per-sample,
    before x100).
    """
    p = _clean(pred).lower()
    if not refs:
        return 0.0
    return sum(1.0 if r.lower() in p else 0.0 for r in refs) / len(refs)


def ruler_sample_score(task: str, pred: str, refs: List[str]) -> float:
    """Per-sample RULER score in [0, 1], dispatched by task category."""
    fn = string_match_part if task_category(task) == "qa" else string_match_all
    return fn(pred, refs)


def ruler_task_score(task: str, preds: List[str], refs: List[List[str]]) -> float:
    """Aggregate RULER score for one task as a percentage in [0, 100].

    Mirrors kvpress / KVCache-Factory: mean of per-sample scores * 100, rounded
    to 2 dp.
    """
    if not preds:
        return 0.0
    total = sum(ruler_sample_score(task, p, r) for p, r in zip(preds, refs))
    return round(total / len(preds) * 100, 2)
